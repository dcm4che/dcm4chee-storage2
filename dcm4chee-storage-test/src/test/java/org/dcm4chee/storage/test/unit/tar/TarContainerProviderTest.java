    @Test
    public void testWriteEntriesTo() throws Exception {
        Path srcEntryPath = createFile(ENTRY, ENTRY_FILE);
        Path targetTarPath = dir.getPath().resolve(NAME);
        try (OutputStream out = Files.newOutputStream(targetTarPath)) {
            provider.writeEntriesTo(storageCtx, makeEntries(srcEntryPath), out);
        }
        assertEquals(TAR.length, Files.size(targetTarPath));
        try (TarArchiveInputStream expectedTar = new TarArchiveInputStream(
                new ByteArrayInputStream(TAR));
                TarArchiveInputStream actualTar = new TarArchiveInputStream(
                        Files.newInputStream(targetTarPath))) {
            assertTarEquals(expectedTar, actualTar);
        }
    }

    private static void assertTarEquals(TarArchiveInputStream expectedTar,
            TarArchiveInputStream actualTar) throws IOException {
        TarArchiveEntry expected;
        TarArchiveEntry actual;
        while ((expected = expectedTar.getNextTarEntry()) != null) {
            actual = actualTar.getNextTarEntry();
            assertNotNull(actual);
            assertTarEntryEquals(expected, actual);
        }
    }

    private static void assertTarEntryEquals(TarArchiveEntry expected,
            TarArchiveEntry actual) {
        assertEquals(expected.getName(), actual.getName());
        assertEquals(expected.getSize(), actual.getSize());
    }

